
mi_tupla = ('indice cero', 2 , 3.0 , 'indice tres') # tupla de cuatro items 

print (mi_tupla[0])
print (mi_tupla[2])
print (mi_tupla[-1])
print (mi_tupla[-2])
print (mi_tupla[1:3])  # devuelve ( 2, 3.0)
print (mi_tupla[:3])  # toma desde el principio de la tupla
print (mi_tupla[1:])  #toma desde el indice de inicio hasta el final de la tupla

